package org.mrmat.hello.java.java8.optional;

/**
 * A model of a USB mouse
 *
 * @author imfeldma
 */
public class Mouse {

    private String name;

    public Mouse(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
